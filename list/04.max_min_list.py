
# Example 4: Finding the Maximum Element
my_list4 = [10, 25, 5, 50, 30]
max_value = max(my_list4)
print("Example 4: Maximum value in list4:", max_value)
# Expected Output: Example 4: Maximum value in list4: 50

# Example 5: Finding the Minimum Element
my_list5 = [10, 25, 5, 50, 30]
min_value = min(my_list5)
print("Example 5: Minimum value in list5:", min_value)
# Expected Output: Example 5: Minimum value in list5: 5


